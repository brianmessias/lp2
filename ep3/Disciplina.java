public class Disciplina{
	private String nome;
	private String professor;

	public void setNome(String nome){
		this.nome = nome;
	}
	public String getNome(){
		return nome;
	}
	public void setProfessor(String professor){ 
		this.professor = professor;
        }
        public String getProfessor(){ 
		return professor;
        }

}
